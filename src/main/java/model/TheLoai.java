package model;

public class TheLoai {
    private String maTL;
    private String tenTL;
    private String moTa;

    public String getMaTL() {
        return maTL;
    }
    public void setMaTL(String maTL) {
        this.maTL = maTL;
    }

    public String getTenTL() {
        return tenTL;
    }
    public void setTenTL(String tenTL) {
        this.tenTL = tenTL;
    }

    public String getMoTa() {
        return moTa;
    }
    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }
}